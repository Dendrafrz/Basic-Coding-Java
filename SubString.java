public class SubString {
    public static void main(String[] args) {
        String kalimat1 = "Fahrezi hebat";
        System.out.println(kalimat1.substring(8, 13));
        // substring diambil dari index pertama huruf "f" index 1
        // fahrezi ada 7 spasi ke 8
        // gimana caranya menghasilkan "hebat";

        String kalimat2 = "Naufal hebat";
        System.out.println(kalimat2.charAt(8));
        // charAt untuk mengambil karakter awal HURUF n = 0 , sedangkan a = 1 (hanya
        // mengambil satu karakter)
        // gimana caranya dapatin charater e
    }
}
